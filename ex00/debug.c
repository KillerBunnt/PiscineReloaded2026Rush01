#include "rush01.h"

void	debug_grid(int grid[6][6])
{
	int	y;
	int	x;

	y = 0;
	x = 0;
	while (y < 6)
	{
		x = 0;
		while (x < 6)
		{
			if (grid[y][x] == 0)
				ft_putchar(' ');
			else
				ft_putchar(grid[y][x] + '0');
			x++;
		}
		ft_putchar('\n');
		y++;
	}
}
